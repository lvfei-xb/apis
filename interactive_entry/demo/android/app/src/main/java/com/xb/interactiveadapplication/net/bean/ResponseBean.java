package com.xb.interactiveadapplication.net.bean;

public class ResponseBean {
    private String imageUrl;
    private String landingUrl;
    private String reportExposeUrl;
    private String reportClickUrl;

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getLandingUrl() {
        return landingUrl;
    }

    public void setLandingUrl(String landingUrl) {
        this.landingUrl = landingUrl;
    }

    public String getReportExposeUrl() {
        return reportExposeUrl;
    }

    public void setReportExposeUrl(String reportExposeUrl) {
        this.reportExposeUrl = reportExposeUrl;
    }

    public String getReportClickUrl() {
        return reportClickUrl;
    }

    public void setReportClickUrl(String reportClickUrl) {
        this.reportClickUrl = reportClickUrl;
    }
}
